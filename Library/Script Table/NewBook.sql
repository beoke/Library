CREATE TABLE NewBook
(
    bId INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
    bName VARCHAR(150) NOT NULL DEFAULT (''),
    bAuthor VARCHAR(150) NOT NULL DEFAULT (''),
    bPublic VARCHAR(150) NOT NULL DEFAULT (''),
    bPDate VARCHAR(50) NOT NULL DEFAULT (''),
    bPrice BIGINT NOT NULL DEFAULT (0),
    bQuan BIGINT NOT NULL DEFAULT (0)
);


